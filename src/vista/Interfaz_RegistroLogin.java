    private void btnActivarRootActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActivarRootActionPerformed
        // TODO add your handling code here:
        String passR =  DigestUtils.md5Hex(this.passRoot.getText());
        Control_registroRoot cr = new Control_registroRoot(null,null,null,null,null,null,0,null);
        if (this.passRoot.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo codigo root esta vacio!!!", "Validando Datos",
                JOptionPane.ERROR_MESSAGE);
        }else{
            if (cr.consulta_root_id(passR) == null) {
                JOptionPane.showMessageDialog(null, "Codigo Root Invalido!!!", "Validando Datos",
                JOptionPane.ERROR_MESSAGE);
            }else{
                this.habilitarCampo();
                this.estadoPermiso = true;
                this.revalidate();
            }
        }
    }//GEN-LAST:event_btnActivarRootActionPerformed

