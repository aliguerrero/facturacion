    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String nom,ape,cedu,user,pass,res;
        int pre,sta;
        
        if (this.campoPass.getText().equals(this.campoPass2.getText())) {
            
            nom = this.campoNombre.getText();
            ape = this.campoApellido.getText();
            cedu = this.campoCedula.getText();
            user = this.campoUser.getText();
            pass = DigestUtils.md5Hex(this.campoPass.getText());
            pre  = this.comboPre.getSelectedIndex();
            res  = this.campoRes.getText();
            sta = USER;
            
            
            Control_login cl = new Control_login(nom,ape,cedu,user,pass,pre,res,sta);
            
            if (!nom.equals("") || !ape.equals("") || !cedu.equals("") || !user.equals("") || !pass.equals("") || pre!=0 || !res.equals("") || sta !=0)
            {   
                if (this.estadoUser == false) {
                    JOptionPane.showMessageDialog(null, "Existe un Usuario registrado con el mismo Username, por favor cambiar!!!", "Validando Datos",
                    JOptionPane.ERROR_MESSAGE);
                    this.campoUser.setText("");
                    this.revalidate();
                }else{
                    if(cl.ingresar_login())
                    {
                        JOptionPane.showMessageDialog(null,"El usuario se registro con exito","Mensaje",JOptionPane.INFORMATION_MESSAGE);
                        limpiar();
                        this.inhabilitarCampo();
                        this.iconVeri1.setVisible(false);
                        this.revalidate();

                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null,"Error al registar el usuario ","Mensaje",JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
           else{
                JOptionPane.showMessageDialog(this, "Hay campos obligatorios");  
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Verifique los datos a registrar, no deje campos vaciosÂ¡ ","Mensaje",JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

